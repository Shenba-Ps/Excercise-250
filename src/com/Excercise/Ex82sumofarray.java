package com.Excercise;
//Write a Java program to sum values of an array
public class Ex82sumofarray {
    public static void main(String[] args) {
        int[] a={10,11,12,13,14,15,17};
        int sum=0;
        for (int i:a){
            sum +=i;
            System.out.println(sum);
        }


    }
}
